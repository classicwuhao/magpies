package util;

public final class Element<R>{
	private R value;
	private int index;
	private String alias;

	public Element(){}
	public Element(R v, int i, String a){value=v;index=i;alias=a;}
	public R getValue(){return value;}
	public void setValue(R v){value=v;}
	public void setIndex(int k){index=k;}
	public void setAlias(String s){alias=s;}
	public String getAlias(){return alias;};
	public int getIndex(){return index;}
	public String toString(){
		return value+":"+index;
	}
}
